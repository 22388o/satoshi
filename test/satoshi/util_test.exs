defmodule Satoshi.UtilTest do
  use ExUnit.Case, async: true
  doctest Satoshi.Util

  alias Satoshi.Util

  test "powmod" do
    prime = 115792089237316195423570985008687907853269984665640564039457584007908834671663
    val = 65341020041517633956166170261014086368942546761318486551877808671514674964848

    assert Util.powmod(val, prime - 2, prime) == 83174505189910067536517124096019359197644205712500122884473429251812128958118
  end
end
